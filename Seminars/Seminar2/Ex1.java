package Seminars.Seminar2;

import java.util.InputMismatchException;
import java.util.Scanner;

public class Ex1 {
    public static void main(String[] args){
        // Создаем объект Scanner для чтения ввода с клавиатуры
        Scanner scanner = new Scanner(System.in);
        
        // Создаем массив целых чисел длиной 10
        int[] arr = new int[10];
        
        // Выводим приглашение для ввода индекса
        System.out.println("Input index: ");
        
        int index = 0; // Переменная для хранения введенного индекса
        
        // Пытаемся прочитать ввод пользователя
        try{
            index = scanner.nextInt(); // Считываем целое число с клавиатуры
        } catch (InputMismatchException e){
            // Если пользователь ввел не целое число, обрабатываем исключение InputMismatchException
            System.out.println("Вы ввели не целое число!");
        }
        
        // Пытаемся присвоить элементу массива значение 1 по указанному индексу
        try{
            arr[index] = 1; // Присваиваем элементу массива значение 1
        } catch (ArrayIndexOutOfBoundsException e){
            // Если индекс выходит за пределы массива, обрабатываем исключение ArrayIndexOutOfBoundsException
            System.out.println("Выход за пределы массива!");
        }
    }
}


// Основные моменты:

// Ввод данных: Создается объект Scanner для считывания ввода с клавиатуры. Пользователю предлагается ввести индекс.
// Обработка ввода: С помощью конструкции try-catch пытаемся считать целое число с клавиатуры. Если ввод пользователя не является целым числом, выбрасывается исключение InputMismatchException, и программа выводит сообщение об ошибке.
// Доступ к массиву: Считанный индекс используется для доступа к элементу массива. Если индекс выходит за пределы массива, выбрасывается исключение ArrayIndexOutOfBoundsException, и программа выводит сообщение об ошибке.
// Обработка исключений: Обе операции, связанные с вводом и доступом к массиву, обернуты в блоки try-catch, чтобы программу не прерывало выброшенное исключение. Вместо этого программа выводит соответствующее сообщение об ошибке.